#!/usr/bin/env node
/**
 * ping-indexnow.js — IndexNow submitter
 *
 * Notifies the IndexNow network (Bing, Yandex, Seznam, and increasingly
 * honored by Google) that this site's URLs have changed, so they re-crawl
 * within minutes instead of waiting for the next scheduled crawl.
 *
 * How it works:
 *   1. Reads sitemap.xml at the repo root and extracts every <loc> URL.
 *   2. POSTs them in one batch to https://api.indexnow.org/indexnow as
 *      JSON: { host, key, keyLocation, urlList }.
 *   3. IndexNow verifies ownership by fetching the key file we host at
 *      https://virarprint.in/<KEY>.txt (also generated by this script).
 *
 * Ownership / the key file:
 *   The key is a fixed 32-char lowercase hex string (see KEY below). The
 *   matching verification file "<KEY>.txt" lives at the repo root and is
 *   (re)written by this script on every run, so the file and the key can
 *   never drift. Commit that .txt file — GitHub Pages must serve it.
 *
 * Fail-safe by design:
 *   This script must NEVER break a deploy. Any error (no sitemap, network
 *   hiccup, non-2xx response) is logged and the process exits 0.
 *
 * Usage:
 *   node scripts/ping-indexnow.js            # parse + submit
 *   node scripts/ping-indexnow.js --dry-run  # parse + write key file, no POST
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SITEMAP = path.join(ROOT, 'sitemap.xml');

const HOST = 'virarprint.in';
const ENDPOINT = 'https://api.indexnow.org/indexnow';

/**
 * IndexNow API key for virarprint.in.
 * Fixed 32-char lowercase hex string. Document/rotate by changing this
 * value AND the hosted key file (this script rewrites the file to match).
 */
const KEY = 'f2a5c7686a8f412b0e9e78465d440d6e';
const KEY_LOCATION = `https://${HOST}/${KEY}.txt`;
const KEY_FILE = path.join(ROOT, `${KEY}.txt`);

const DRY_RUN = process.argv.includes('--dry-run');

/** Exit cleanly — never propagate a non-zero status to the build. */
function bailOut(message) {
  console.log(`ℹ IndexNow: ${message} — skipping submit (exit 0).`);
  process.exit(0);
}

/** Ensure the <KEY>.txt verification file exists at the repo root. */
function ensureKeyFile() {
  try {
    const current = fs.existsSync(KEY_FILE) ? fs.readFileSync(KEY_FILE, 'utf8').trim() : null;
    if (current !== KEY) {
      fs.writeFileSync(KEY_FILE, KEY, 'utf8');
      console.log(`🔑 Wrote key file: ${path.relative(ROOT, KEY_FILE)}`);
    } else {
      console.log(`🔑 Key file present: ${path.relative(ROOT, KEY_FILE)}`);
    }
  } catch (err) {
    console.log(`ℹ IndexNow: could not write key file (${err.message}) — continuing.`);
  }
}

/** Extract all <loc>…</loc> URLs from the sitemap XML. */
function extractLocs(xml) {
  const locs = [];
  const re = /<loc>\s*([^<\s]+)\s*<\/loc>/g;
  let m;
  while ((m = re.exec(xml)) !== null) locs.push(m[1].trim());
  return locs;
}

async function main() {
  // Always keep the key file in sync, even on a dry run.
  ensureKeyFile();

  if (!fs.existsSync(SITEMAP)) {
    bailOut('sitemap.xml not found (run build:sitemap first)');
  }

  let urlList;
  try {
    urlList = extractLocs(fs.readFileSync(SITEMAP, 'utf8'));
  } catch (err) {
    bailOut(`could not read sitemap.xml (${err.message})`);
  }

  if (!urlList.length) {
    bailOut('no <loc> URLs found in sitemap.xml');
  }

  console.log(`📡 IndexNow: ${urlList.length} URL(s) for ${HOST}`);
  urlList.forEach((u) => console.log(`   • ${u}`));

  const payload = { host: HOST, key: KEY, keyLocation: KEY_LOCATION, urlList };

  if (DRY_RUN) {
    console.log('\n🧪 --dry-run: payload built, not submitting:');
    console.log(JSON.stringify(payload, null, 2));
    process.exit(0);
  }

  if (typeof fetch !== 'function') {
    bailOut('global fetch unavailable (needs Node 18+)');
  }

  try {
    const res = await fetch(ENDPOINT, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json; charset=utf-8',
        Accept: 'application/json',
      },
      body: JSON.stringify(payload),
    });

    // IndexNow returns 200 or 202 on success; treat anything else as a
    // soft failure (logged, exit 0).
    if (res.status === 200 || res.status === 202) {
      console.log(`\n✅ IndexNow accepted the submission (HTTP ${res.status}).`);
    } else {
      const body = await res.text().catch(() => '');
      console.log(`\n⚠ IndexNow responded HTTP ${res.status}. ${body}`.trim());
      console.log('   (Non-fatal — exiting 0 so the deploy is unaffected.)');
    }
  } catch (err) {
    console.log(`\n⚠ IndexNow request failed: ${err.message}`);
    console.log('   (Non-fatal — exiting 0 so the deploy is unaffected.)');
  }

  process.exit(0);
}

main();
